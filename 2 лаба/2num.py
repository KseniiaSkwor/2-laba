if a > 36 and a % 2 == 0:
    print("Ваше место - боковое верхнее")
elif a > 36 and a % 2 != 0:
    print("Ваше место - боковое нижнее")
elif a <= 36 and a % 2 != 0:
    print("Ваше место - купе нижнее")
elif a <= 36 and a % 2 == 0:
    print("Ваше место - купе верхнее")
